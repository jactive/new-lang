#include <iostream>

using namespace std;

int main() {
   cout << "decimal, " << dec << 12 << endl;
   cout << "in oct, " << oct << 12 << endl;
   cout << "in hex, " << hex << 12 << endl;
   cout << "a floting-point number: " << 3.14159 << endl;
   cout << "non-printing character (escape): " << char(27) << endl;
   cout << "ascii char: " << char(65) << endl;
}
